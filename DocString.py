def add(x: int, y: int) -> int:
    """
    Calculate the sum of two numbers.

    Parameters
    ----------
    x : int
        The first integer number.
    y : int
        The second integer number.

    Returns
    -------
    int
        The sum of x and y.
    """
    return x + y

